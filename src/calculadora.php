<?php

class Calculadora{

    public function _constructor(){

    }

    public function sum($n1,$n2){
            return $n1+$n2;
    }

    public function sub($n1,$n2){
        return $n1+$n2;
    }

    public function divide($n1,$n2){
        return $n1/$n2;
    }

    public function multiply($n1,$n2){
        return $n1*$n2;
    }

}
?>